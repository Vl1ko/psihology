def calculate_stress_level(answers):
    """
    Рассчитывает уровень стресса на основе ответов пользователя
    """
    total_score = sum(int(value) for value in answers.values())
    
    # Определение уровня стресса на основе общей суммы баллов
    if total_score <= 10:
        level = "Низкий"
        recommendation = """
            У вас низкий уровень стресса. Продолжайте поддерживать свое психологическое 
            благополучие через регулярные физические упражнения, сбалансированное питание и здоровый сон.
        """
    elif total_score <= 20:
        level = "Средний"
        recommendation = """
            У вас средний уровень стресса. Рекомендуется больше времени уделять отдыху, 
            практиковать дыхательные упражнения и медитацию, а также заниматься приятными 
            для вас делами.
        """
    elif total_score <= 30:
        level = "Повышенный"
        recommendation = """
            У вас повышенный уровень стресса. Рекомендуется пересмотреть свой распорядок дня, 
            сократить рабочую нагрузку, если возможно, регулярно практиковать техники релаксации 
            и больше времени проводить на свежем воздухе.
        """
    else:
        level = "Высокий"
        recommendation = """
            У вас высокий уровень стресса. Рекомендуется обратиться за помощью к специалисту 
            (психологу или психотерапевту). В краткосрочной перспективе важно обеспечить себе 
            достаточный отдых, уменьшить количество стрессовых ситуаций и использовать техники 
            релаксации.
        """
    
    return level, recommendation 